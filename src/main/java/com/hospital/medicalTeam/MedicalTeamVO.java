package com.hospital.medicalTeam;

public class MedicalTeamVO {
	private int num;
	private String name;
	private String med_office;
	private String pro_field;
	private String career;
	private String major_act;
	private int mid;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMed_office() {
		return med_office;
	}
	public void setMed_office(String med_office) {
		this.med_office = med_office;
	}
	public String getPro_field() {
		return pro_field;
	}
	public void setPro_field(String pro_field) {
		this.pro_field = pro_field;
	}
	public String getCareer() {
		return career;
	}
	public void setCareer(String career) {
		this.career = career;
	}
	public String getMajor_act() {
		return major_act;
	}
	public void setMajor_act(String major_act) {
		this.major_act = major_act;
	}
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	
}
